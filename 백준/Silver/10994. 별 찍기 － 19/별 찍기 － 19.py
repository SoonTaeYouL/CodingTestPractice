import sys

num = int(sys.stdin.readline())
'''
************* 0,0  4     13-4x
*           *
* ********* * 2,2  3     13- 4x(n-1)+1 //2
* *       * *
* * ***** * * 4,4  2     13 - 4x1 +1 //2 
* * *   * * *
* * * * * * * 6,6  1     13- 1 //2
* * *   * * *
* * ***** * *
* *       * *
* ********* *
*           *
*************
'''
l = [[ " " for _ in range(4*(num-1)+1)] for _ in range(4*(num-1)+1)]

def sta(n):
    if n==1:
        l[(len(l)-4*(n-1)-1)//2][(len(l)-4*(n-1)-1)//2]='*'
        return
    else:
        for i in range(4*(n-1)+1):
            l[(len(l)-4*(n-1)-1)//2][(len(l)-4*(n-1)-1)//2+i]='*'
            l[(len(l)-4*(n-1)-1)//2+i][(len(l)-4*(n-1)-1)//2]='*'
            l[(len(l)-4*(n-1)-1)//2+i][(len(l)-4*(n-1)-1)//2+4*(n-1)]='*'
            l[len(l)-(len(l)-4*(n-1)-1)//2-1][(len(l)-4*(n-1)-1)//2+i]='*'
        sta(n-1)
sta(num)
for i in range(4*(num-1)+1):
    print(*l[i],sep='')